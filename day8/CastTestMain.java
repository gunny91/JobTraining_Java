package pkg08;

public class CastTestMain {

	public static void main(String[] args) {
		
		// Basic data type 
		// int, float, double, char, boolean
		
		// Reference data type
		// Array, class(String, Math, Scanner, Random, List, Map, Hash), Interface
		//Integer, Float, Double, Chracter, Boolean etc...
		
		
		//Implicit
		// Small data type to Big data type
		double d= 100;
		System.out.println("Implicit Casting : "+d);
		
		
		//Explicit 
		//Big data type to Small data type.
		int i = (int) 3.14;
		
		System.out.println("Explicit Casting : "+i);
		
		
		
	
		
		
	}

}
